#!/bin/bash
set -e

# ==============================================================================
# LuminaWAF: The Iron Benchmark (Żelazny Glajt)
# ==============================================================================
# Skrypt weryfikujący wydajność WAF-a w warunkach kontrolowanych.
# Generuje ostateczny raport sprzętowy opatrzony sumą kontrolną SHA256.
# Żadnych modyfikacji ręcznych. Pełna deterministyczna weryfikacja.
# ==============================================================================

REPORT_DIR="final_reports"
mkdir -p "$REPORT_DIR"

TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
REPORT_FILE="$REPORT_DIR/LuminaWAF_Iron_Report_${TIMESTAMP}.md"

echo "[*] Generating Iron Benchmark Report: $REPORT_FILE"

cat << 'EOF' > "$REPORT_FILE"
# LuminaWAF: The Iron Benchmark (Integrity Checksummed)

This document is automatically generated by `tools/iron_benchmark.sh`.
It represents the definitive, reproducible performance measurement of the LuminaWAF microarchitecture.
Manual modifications to this document will invalidate the local integrity checksum located at the bottom.
EOF

echo "## 0. Experimental Methodology & Measurement Contract" >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "### Measurement Contract" >> "$REPORT_FILE"
echo "All results represent wall-clock measurements unless otherwise specified. CPU time is reported separately to decouple scheduler noise." >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "### Reproducibility Constraints" >> "$REPORT_FILE"
echo "Benchmarks require:" >> "$REPORT_FILE"
echo "- CPU isolation (\`isolcpus\`)" >> "$REPORT_FILE"
echo "- Performance governor (CPU frequency scaling: turbo boost enabled, observed max 3.5 GHz) or static pinning" >> "$REPORT_FILE"
echo "- No SMT interference (recommended)" >> "$REPORT_FILE"
echo "- System must be otherwise idle except for benchmark process affinity pinned to isolated cores (Load Average reflects benchmarking thread saturation only)." >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "### Workload Definition" >> "$REPORT_FILE"
echo "Workload consists of:" >> "$REPORT_FILE"
echo "- Normalized HTTP query strings" >> "$REPORT_FILE"
echo "- SecLists adversarial payloads" >> "$REPORT_FILE"
echo "- URL-encoded mixed encoding ratios (0–100%)" >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "### Statistical Interpretation Rule" >> "$REPORT_FILE"
echo "Primary metric is the **median**; mean is secondary due to skew sensitivity in tail latency workloads." >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "### Terminology & Definitions" >> "$REPORT_FILE"
echo "- **Warm cache**: Repeated invocation with dataset resident in L1/L2 cache." >> "$REPORT_FILE"
echo "- **Cold cache**: Dataset forced eviction via controlled memory pressure." >> "$REPORT_FILE"
echo "- **Full Stack Overhead**: End-to-end HTTP benchmarks (wrk2) measure full stack overhead including NGINX routing, not pure WAF evaluation cost." >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "---" >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "## 1. Hardware & Environment Probe" >> "$REPORT_FILE"

echo '```text' >> "$REPORT_FILE"
echo "Date: $(date)" >> "$REPORT_FILE"
echo "Hostname: $(hostname)" >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "--- OS Kernel ---" >> "$REPORT_FILE"
uname -a >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "--- CPU Architecture ---" >> "$REPORT_FILE"
lscpu | grep -E "Model name|Architecture|CPU\(s\)|Thread|L1d|L1i|L2|L3" >> "$REPORT_FILE"
echo "" >> "$REPORT_FILE"
echo "--- Memory ---" >> "$REPORT_FILE"
free -h >> "$REPORT_FILE"
echo '```' >> "$REPORT_FILE"

echo "" >> "$REPORT_FILE"
echo "## 2. Microbenchmark: The Rule Explosion Phenomenon (1000 Rules vs AVX2)" >> "$REPORT_FILE"
echo "Testing the scaling limits of PCRE regex engines (ModSecurity) vs Branchless SIMD Pipeline (LuminaWAF)." >> "$REPORT_FILE"
echo '```text' >> "$REPORT_FILE"

# Rebuild in Release to ensure NDEBUG is set
echo "[*] Ensuring Release build (clean build)..."
mkdir -p build && cd build
rm -rf *
cmake -DCMAKE_BUILD_TYPE=Release .. > /dev/null
make -j4 > /dev/null
cd ..

echo "[*] Executing micro_bench..."
./build/micro_bench >> "$REPORT_FILE" 2>&1
echo '```' >> "$REPORT_FILE"

echo "" >> "$REPORT_FILE"
echo "## 3. Synthetic Benchmark: Adversarial Branch Penalty Evaluation" >> "$REPORT_FILE"
echo "Measuring the degradation of the parser under increasing saturation of URL-encoded `%XX` sequences." >> "$REPORT_FILE"
echo '```text' >> "$REPORT_FILE"
echo "[*] Executing synthetic_bench..."
./build/synthetic_bench >> "$REPORT_FILE" 2>&1
echo '```' >> "$REPORT_FILE"

echo "" >> "$REPORT_FILE"
echo "## 4. Corpus Benchmark: L1/LLC Cache Bandwidth (Mmap)" >> "$REPORT_FILE"
echo "Testing maximum memory saturation via memory-mapped massive log datasets." >> "$REPORT_FILE"
echo '```text' >> "$REPORT_FILE"
echo "[*] Executing corpus_bench..."
./build/corpus_bench >> "$REPORT_FILE" 2>&1
echo '```' >> "$REPORT_FILE"

echo "" >> "$REPORT_FILE"
echo "## 5. End-to-End Integration Benchmark: NGINX Saturation (wrk2)" >> "$REPORT_FILE"
echo "Simulating adversarial HTTP traffic against ModSecurity and LuminaWAF NGINX instances." >> "$REPORT_FILE"
echo '```text' >> "$REPORT_FILE"
echo "[*] Executing wrk2 saturation benchmark..."
# Force fresh run instead of reading old cache
bash tests/eval_suite/run_wrk2_bench.sh > /dev/null 2>&1 || echo "wrk2 benchmark failed. Ensure NGINX servers are running on 8090, 8081, 8082." >> "$REPORT_FILE"
if [ -f "tests/eval_suite/results/wrk2_saturation.txt" ]; then
    cat tests/eval_suite/results/wrk2_saturation.txt >> "$REPORT_FILE"
else
    echo "Could not find fresh wrk2 results." >> "$REPORT_FILE"
fi
echo '```' >> "$REPORT_FILE"

echo "" >> "$REPORT_FILE"
echo "## 6. Data Parity & Semantic Equivalence (Differential Fuzzing)" >> "$REPORT_FILE"
echo "Proving bit-by-bit correctness of LuminaWAF AVX2 decoding against the ModSecurity scalar engine baseline." >> "$REPORT_FILE"
if [ -f "final_reports/raw_data/diff_fuzzer_results.md" ]; then
    cat final_reports/raw_data/diff_fuzzer_results.md >> "$REPORT_FILE"
else
    echo "Parity test results not found in final_reports/raw_data." >> "$REPORT_FILE"
fi

echo "" >> "$REPORT_FILE"
echo "---" >> "$REPORT_FILE"

echo "[*] Calculating Local Integrity Checksum..."
# Calculate hash of the file content up to this point
HASH=$(sha256sum "$REPORT_FILE" | awk '{print $1}')

# Append the seal
cat << EOF >> "$REPORT_FILE"
### Local Integrity Checksum
**SHA256 Checksum:** \`${HASH}\`
EOF
echo "> *To verify this document: extract all contents up to (but not including) the \"Local Integrity Checksum\" header and run \`sha256sum\`. The hash input strictly locks all runtime logs, system-generated timestamps, and measurements of this specific run.*" >> "$REPORT_FILE"

echo "[*] DONE. Report sealed at: $REPORT_FILE"
echo "[*] SHA256: $HASH"
